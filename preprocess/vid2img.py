import cv2



vid = cv2.VideoCapture('../data/coffee_martini/cam01.mp4')
success, image = vid.read()
count = 0

while success:
    cv2.imwrite("../data/coffee_martini/cam01/%06d.jpg" % count, image)     # save frame as JPEG file
    success,image = vid.read()
    print('Read a new frame: ', success)
    count += 1

